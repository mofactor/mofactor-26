/**
 * Generates src/app/cms-safelist.css from:
 *   1. The static Tailwind class list (tw-classes.ts)
 *   2. Dynamic classes used in published posts (fetched from Convex)
 *
 * Run: npm run generate:safelist
 * Auto-runs via predev / prebuild.
 */

import { writeFileSync, readFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

// 1. Static classes from tw-classes.ts
const { ALL_CLASSES } = await import("../src/lib/tw-classes.ts");

// 2. Dynamic classes from Convex DB (best-effort — skip if unavailable)
let dbClasses = [];
try {
  // Read .env.local for Convex URL
  const envPath = join(root, ".env.local");
  const envContent = readFileSync(envPath, "utf8");
  const convexUrl = envContent
    .split("\n")
    .find((l) => l.startsWith("NEXT_PUBLIC_CONVEX_URL="))
    ?.split("=")[1]
    ?.trim();

  if (convexUrl) {
    const { ConvexHttpClient } = await import("convex/browser");
    const { api } = await import("../convex/_generated/api.js");
    const client = new ConvexHttpClient(convexUrl);
    dbClasses = await client.query(api.posts.allUsedClasses);
    console.log(`  ↳ ${dbClasses.length} classes from DB`);
  }
} catch (err) {
  console.log(`  ↳ Skipping DB classes: ${err.message}`);
}

// 3. Merge and deduplicate
const allClasses = [...new Set([...ALL_CLASSES, ...dbClasses])];

const css = `/* Auto-generated by scripts/generate-safelist.mjs — do not edit */
@source inline("${allClasses.join(" ")}");
`;

const outPath = join(root, "src/app/cms-safelist.css");
writeFileSync(outPath, css);

console.log(`✓ cms-safelist.css — ${allClasses.length} classes (${ALL_CLASSES.length} static + ${dbClasses.length} from DB)`);
